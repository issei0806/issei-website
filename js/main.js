// PORTFOLIO CAROUSEL
// =============================================

document.querySelectorAll('.portfolio-carousel-outer').forEach(outer => {
  const carousel = outer.querySelector('.portfolio-carousel');
  const prev = outer.querySelector('.carousel-nav--prev');
  const next = outer.querySelector('.carousel-nav--next');
  if (!carousel || !prev || !next) return;

  // Wix-like carousel: scroll one card at a time for a smoother feel.
  const getStep = () => {
    const thumb = carousel.querySelector('.portfolio-thumb');
    if (!thumb) return 230;
    const styles = window.getComputedStyle(carousel);
    const gap = parseInt(styles.columnGap || styles.gap || '12', 10) || 12;
    return thumb.offsetWidth + gap;
  };

  prev.addEventListener('click', () => {
    carousel.scrollBy({ left: -getStep(), behavior: 'smooth' });
  });
  next.addEventListener('click', () => {
    carousel.scrollBy({ left: getStep(), behavior: 'smooth' });
  });
});

// =============================================
// HAMBURGER MENU
// =============================================

const hamburger = document.querySelector('.hamburger');
const navDrawer = document.querySelector('.nav-drawer');
const drawerOverlay = document.querySelector('.nav-drawer-overlay');

function openDrawer() {
  hamburger.classList.add('open');
  hamburger.setAttribute('aria-expanded', 'true');
  navDrawer.classList.add('open');
  navDrawer.setAttribute('aria-hidden', 'false');
  drawerOverlay.classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeDrawer() {
  hamburger.classList.remove('open');
  hamburger.setAttribute('aria-expanded', 'false');
  navDrawer.classList.remove('open');
  navDrawer.setAttribute('aria-hidden', 'true');
  drawerOverlay.classList.remove('open');
  document.body.style.overflow = '';
}

if (hamburger) {
  hamburger.addEventListener('click', () => {
    hamburger.classList.contains('open') ? closeDrawer() : openDrawer();
  });
}
if (drawerOverlay) drawerOverlay.addEventListener('click', closeDrawer);
if (navDrawer) navDrawer.querySelectorAll('a').forEach(a => a.addEventListener('click', closeDrawer));

// =============================================
// CONTACT FORM
// =============================================

const form = document.getElementById('contact-form');
if (form) {
  form.addEventListener('submit', async (e) => {
    e.preventDefault();
    const toast = document.querySelector('.toast');
    const btn = form.querySelector('.btn-send');
    btn.disabled = true;

    const res = await fetch('/', {
      method: 'POST',
      headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
      body: new URLSearchParams(new FormData(form)).toString()
    }).catch(() => null);

    const ok = res && res.ok;
    toast.textContent = ok
      ? translations[currentLang]['toast.msg']
      : (currentLang === 'ja' ? '送信に失敗しました。もう一度お試しください。' : 'Failed to send. Please try again.');
    toast.classList.add('show');
    if (ok) form.reset();
    btn.disabled = false;
    setTimeout(() => toast.classList.remove('show'), 3500);
  });
}
